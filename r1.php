<h1><b>What Causes Airway Obstruction?</b></h1>
<p>An airway obstruction is a blockage in any part of the airway. The airway is a complex system of tubes that conveys inhaled air from your nose and mouth into your lungs. An obstruction may partially or totally prevent air from getting into your lungs.</p>
<p>Some airway obstructions are minor, while others are life-threatening emergencies that require immediate medical attention.</p>
<h1><b>Prevetion</b></h1>
<ul>
<li>Avoid drinking a lot of alcohol before eating.</li>
<li>Eat small bites of food.</li>
<li>Eat slowly.</li>
<li>Supervise small children when eating.</li>
<li>Chew thoroughly before swallowing.</li>
<li>Make sure your dentures fit properly.</li>
<li>Keep small objects away from children.</li>
<li>Don’t smoke.</li>	
<li>Visit your doctor regularly if you have a condition that can cause a chronic airway obstruction.</li>
</ul>
<h1><b>Home remedies</b></h1>
<h2>Turmeric</h2>
<p>Turmeric is a spice often used in East Indian foods. A 2011 study found turmeric provided more anti-inflammatory effects than ginger. Turmeric also increases antioxidant activity. That means it may help reduce irritation and boost your immunity. </p>
<ul>
	<li>Add fresh turmeric to salads or use it to make pickles.</li>
	<li>Mix 1/2 teaspoon of powdered turmeric with 1 teaspoon of honey to make a paste. Consume the paste 1 to 3 times per day while symptoms last. </li>
	<li>Take turmeric in capsule form as directed. </li>
	<li>Use powdered or fresh turmeric to make tea. </li>
</ul>
<h2>Ginger</h2>
<p>Some researchers have found evidence that ginger can have an anti-inflammatory effect against respiratory infection. You can take ginger in several ways:</p>
<ul>
	<li>Chew dried, crystallized ginger.</li>
	<li>Use fresh ginger to make tea.</li>
	<li>Eat it raw or add it to food.</li>
	<li>Take it in capsule form as directed.</li>
</ul>
<p>It’s safest to use ginger in a natural form, rather than in capsules or supplements. You may be sensitive to ginger, so take it in small amounts if you’re not used to it. Eating occasional ginger is safe for everyone, but do not take ginger as a supplement or medication if you:</p>
<ul>
	<li>are pregnant or breastfeeding</li>
	<li>have heart problems</li>
	<li>have any type of blood disorder</li>
</ul>
