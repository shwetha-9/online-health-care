<h1><b>Kyphosis Deformity of Spine</b></h1>
<p>Kyphosis, also known as roundback or hunchback, is a condition in which the spine in the upper back has an excessive curvature. The upper back, or thoracic region of the spine, is supposed to have a slight natural curve. The spine naturally curves in the neck, upper back, and lower back to help absorb shock and support the weight of the head. Kyphosis occurs when this natural arch is larger than normal.</p>
<p>If you have kyphosis, you may have a visible hump on your upper back. From the side, your upper back may be noticeably rounded or protruding. In addition, people with hunchback appear to be slouching and have noticeable rounding of the shoulders. Kyphosis can lead to excess pressure on the spine, causing pain. It may also cause breathing difficulties due to pressure put on the lungs.</p>
