<h1>Prevention</h1>
<p><b>Build more lean muscle.</b> Maintain, or even increase, your metabolism by continuing to build lean muscle. "Muscle has a higher metabolism than fat does," explains Emily Banes, RD, clinical dietitian at Houston Northwest Medical Center. If you don't yet train with weights, add this type of exercise to your overall program now. If you do, increase the amount of weight you're working with to keep yourself challenged.</p>
