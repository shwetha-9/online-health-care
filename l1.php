<h1><b>How Is Lazy Eye Diagnosed?</b></h1>
<p>Lazy eye usually occurs in only one eye. When it first occurs, parents and children often don’t notice the condition. It’s important to get routine eye exams as an infant and child, even if you show no outward symptoms of eye problems. The American Optometric Association recommends that children have eye exams when they are 6 months old and 3 years old. After that, children should receive routine exams every two years, or more frequently, from age 6 to 18.</p>
