<h1><b>Lazy Eye</b></h1>
<p>The medical term for lazy eye is “amblyopia.” Lazy eye occurs when your brain favors one eye, often due to poor vision in your other eye. Eventually, your brain might ignore signals from your weak or “lazy” eye. The condition can result in vision impairment and loss of depth perception.Your affected eye doesn’t necessarily look different, although it may “wander” in different directions. That’s where the term “lazy” comes from. The condition will usually only affect one of your eyes, but in certain circumstances, the vision in both of your eyes can be affected.</p>
<p>The condition usually occurs in children. According to the Mayo Clinic, it’s the leading cause of vision problems among children.It’s important to note that lazy eye isn’t the same as a crossed or turned eye. That condition is called strabismus. However, strabismus can lead to lazy eye if your crossed eye gets much less use than your uncrossed one.If your lazy eye goes untreated, temporary or permanent loss of vision can occur. This can include loss of both depth perception and 3D vision.</p>
