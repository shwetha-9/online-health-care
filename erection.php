<h1><b>What Are Erection Problems?</b></h1>
<p>The causes of ED can be physical, psychological, or a combination of the two. Physical causes of ED are more common in older men. They occur because of disorders that can affect the nerves and blood vessels responsible for causing an erection.<br>Physical causes of erectile problems include:</p>
<ul>
	<li>heart disease</li>
	<li>hardening of the arteries</li>
	<li>high blood pressure</li>
	<li>diabetes</li>
	<li>obesity</li>
	<li>high cholesterol</li>
	<li>Parkinson’s disease</li>
	<li>multiple sclerosis</li>
	<li>Peyronie’s disease</li>
	<li>certain medications, including diuretics, beta blockers, muscle relaxers, or antidepressants</li>
	<li>alcoholism or substance abuse</li>
	<li>long-term tobacco use</li>
	<li>trauma or injury to the spinal cord or genital region</li>
	<li>congenital genitalia problems</li>
	<li>liver or kidney disease</li>
	<li>treatment for prostate problems</li>
</ul>
<h1><b>How Can You Treat Erection Problems?</b></h1>
<p>According to American Family Physician, severity of ED is graded on a three-point scale: mild, moderate, and complete. The first step to treating your ED is identifying where you fall on this scale. Additionally, the causes of ED typically pinpoints at least one or more phases of male sexual response. These include: desire, arousal, orgasm, and relaxation. A problem in one or more of these may be the source of the problem.<br>The good news is once the severity and cause of the disorder is found, ED becomes easy to treat.<br>Options for treating erection issues may include:</p>
<ul>
	<li>medications injected into the penis</li>
	<li>medication injected into the urethra</li>
	<li>medications taken by mouth</li>
	<li>surgery</li>
	<li>vacuum devices</li>
</ul>
<h3>Lifestyle Changes</h3>
<p>Many of the physical causes of erection problems are related to lifestyle choices. You may want to consider the following changes:</p>
<ul>
	<li>stopping tobacco use</li>
	<li>drinking less alcohol</li>
	<li>getting plenty of rest</li>
	<li>eating a healthy diet</li>
	<li>exercising regularly</li>
	<li>talking with your partner about sexual issues</li>
</ul>
<p>If changes in your lifestyle don’t reduce your symptoms, contact your doctor to find the root cause of the ED. Your doctor will examine your penis, rectum, and prostate as well as your nervous system function. The doctor will also ask you about current health problems and when your symptoms began.</p>
