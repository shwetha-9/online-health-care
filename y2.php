<h1><b>What Causes Yellow Eyes?</b></h1>
<p>Yellowing of the eyes occurs if you have jaundice. Jaundice occurs when the oxygen-carrying components in the blood, called hemoglobin, break down into bilirubin and your body doesn’t clear the bilirubin. Bilirubin is supposed to move from the liver to the bile ducts, and then your body should release it in your stool. If any part of this process does not occur, bilirubin builds up in your skin and causes it to appear yellow. This also can occur in your eyes. </p><br>
<p>The white portion of the eye is known as the sclera. Healthy eye tissue should appear white. Yellowing of the eyes suggests there’s an underlying medical condition present.</p>
