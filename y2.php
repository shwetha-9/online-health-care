<h1><b>Conditions that affect the liver</b></h1>
<p>The liver performs numerous important functions for the body, including breaking down red blood cells. Conditions that affect the liver’s functioning can lead to yellowing of the eyes. Liver scarring, or cirrhosis, commonly causes this to occur. Conditions that can cause cirrhosis include: </p>
<ul>
	<li>alcohol abuse </li>
	<li>liver cancer</li>
	<li>liver infection</li>
	<li>non-alcoholic fatty liver disease, which is a condition that commonly occurs in obese people </li>
	<li>hepatitis B and C </li>
</ul>
<p>Hepatitis A, D, and E can also cause jaundice, but these are less common causes of jaundice than hepatitis B and C.</p>
