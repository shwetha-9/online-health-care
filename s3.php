<h1><b> Insomnia causes</b></h1>
<p>The causes of your insomnia will depend on the type of sleeplessness you experience.</p>
<p>Short-term insomnia may be caused by stress, an upsetting or traumatic event, or changes to your sleep habits.</p>
<p>Chronic insomnia lasts for at least three months and is usually secondary to another problem or a combination of problems, including:</p>
<ul>
	<li>medical conditions which make it harder to sleep, such as arthritis or back pain</li>
	<li>psychological issues, such as anxiety or depression</li>
	<li>substance use</li>
</ul>
<h1><b>Prevention</b></h1>
<p>Think positive. Avoid going to bed with a negative mindset, such as "If I don't sleep for 8 hours, I will feel terrible tomorrow."</p>
<p>Try to go to sleep at the same time each night and get up at the same time each morning. Try not to take naps during the day because naps may make you less sleepy at night.</p>
<p>Avoid caffeine, nicotine, and alcohol late in the day. Caffeine and nicotine are stimulants and can keep you from falling asleep. Alcohol can cause waking in the night and interferes with sleep quality.</p>
<p>Get regular exercise. Try not to exercise close to bedtime because it may stimulate you and make it hard to fall asleep. Experts suggest not exercising for 4 hours before the time you go to sleep.</p>
<p>Don't eat a heavy meal late in the day. A light snack before bedtime, however, may help you sleep.</p>
<p>Make your sleeping place comfortable. Be sure that it is dark, quiet, and not too warm or too cold. If light is a problem, try a sleeping mask. If noise is a problem, try earplugs or a fan.</p>
<p>Relax before going to bed by reading a book, listening to music, taking a bath, or enjoying another activity you find relaxing.</p>
<h1><b>Home remedies</b></h1>
<p>We’re all familiar with that silly image of the person who resorts to counting sheep when they just can’t seem to fall asleep. But when you’re the one who’s been tossing and turning all night, insomnia is no laughing matter.</p>
<p>As many as one in four adults report suffering from mild insomnia, found a recent Harvard survey. That inability to fall and stay asleep could stem from a short-term issue, or be the result of a lifetime of poor sleep habits. Either way, it can’t be fixed with something like sleeping pills.</p>
<p>Here’s why insomnia happens, why you can’t rely on prescription meds to solve the problem, and how to develop healthier sleep habits for a lifetime of quality snoozing.</p>
<p>In case you missed the memo, being physically active is essential for sleeping well. Mounting evidence shows that people who exercise regularly tend to snooze better than their couch potato counterparts—especially when it comes to those with chronic insomnia.</p>
<p>Not convinced? Consider this. One study, published in the journal SLEEP, concluded that people who get 60 minutes of exercise five days per week have more normal REM sleep than non-exercisers. But you might not need to sweat it out for quite that long to reap the benefits. Other findings show that insomniacs who engage in thirty minute spurts of exercise just three or four times a week sleep for nearly an hour longer than sedentary folks, and wake up less frequently during the night.</p>
