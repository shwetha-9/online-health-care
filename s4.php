<h1><b>What’s Causing My Clammy Skin?</b></h1>
<h1> Clammy skin</h1>
<p>Clammy skin refers to wet or sweaty skin. Sweating is your body’s normal response to overheating. The moisture of sweat has a cooling effect on your skin.</p>
<p>Changes in your body from physical exertion or extreme heat can trigger your sweat glands and cause your skin to become clammy. This is normal. However, clammy skin that occurs for no apparent reason can be the sign of a serious medical condition.</p>
<p>Clammy skin that isn’t a result of physical exertion or a reaction to hot weather can be a symptom of a more serious medical condition. Don’t ignore this symptom. You should always report it to your doctor. In order to relieve clammy skin, the underlying cause must be discovered and treated.</p>
<p>Clammy skin can be a symptom of several conditions, such as a kidney infection or the flu. Other common causes of clammy skin include:</p>
<ul>
<li>panic attacks</li>
<li>an overactive thyroid gland</li>
<li>low blood sugar</li>
<li>hyperhidrosis, which is excessive sweating </li>
<li>internal bleeding</li>
<li>heat exhaustion</li>
</ul>
<p>Clammy skin can also be one of the symptoms associated with a heart attack. A heart attack occurs when a blood clot blocks one of your coronary arteries. Coronary arteries take blood and oxygen to your heart muscle. If your heart muscle doesn’t get enough blood or oxygen, your heart muscle cells will die and your heart won’t work the way it should. Call 911 or go to an emergency room if you believe you’re having a heart attack.</p>
<h1><b>Prevention</b></h1>
<p>Oily skin that is linked to genetics can be difficult to prevent. Even oily skin caused by changes in hormones is difficult to control.</p>
<p>The best prevention for oily skin is for an individual to find a consistent skin care routine that works, and to stick to it.</p>
<p>When oily skin arises, it can be tempting to hide it with makeup. However, certain products, particularly oil-based products, could make symptoms worse or clog the pores.</p>
<p>Water-based makeup may be a better option for some, while others find wearing no makeup works best for their skin.</p>
