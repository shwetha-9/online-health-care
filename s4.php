<h1><b> How is clammy skin treated?</b></h1>
<p>Treatment for clammy skin depends on its underlying cause. Heat exhaustion and dehydration are both treated by rehydrating with fluids using an intravenous (IV) line. You may need to stay in a hospital during your treatment if you have heat exhaustion and symptoms of shock.</p>
<p>You’ll need immediate medical attention if a life-threatening condition, such as shock or a heart attack, is causing your clammy skin.</p>
<p>For a severe allergic reaction or anaphylaxis, you’ll need a medication called epinephrine to counteract your allergic reaction. Epinephrine is a type of adrenaline that stops your body’s reaction to the allergen that’s causing your symptoms.</p>
<p>Clammy skin caused by hormonal imbalances from menopause or andropause (male menopause), can be treated with replacement hormone medication. This medication is only available by prescription.</p>