<h1><b>What Causes Excessive Yawning?</b></h1>
<p>Yawning is a mostly involuntary process of opening the mouth and breathing in deeply, filling the lungs with air. It is a very natural response to being tired. In fact, yawning is usually triggered by sleepiness or fatigue. Some yawns are short, and some last for several seconds before an open-mouthed exhale. Watery eyes, stretching, or audible sighs may accompany yawning.</p>
<p>Researchers are not exactly sure why yawning occurs, but common triggers include fatigue and boredom. Yawns may also occur when you talk about yawning or see or hear someone else yawn. It is believed that contagious yawning may have something to do with social communication. In addition, a study published in the Applied Journal of Basic Medical Research suggests that yawning may help cool the temperature of the brain.</p>
<p>Excessive yawning is yawning that occurs more than once per minute. Although excessive yawning is usually attributed to being sleepy or bored, it may be a symptom of an underlying medical problem. </p>
<p>Certain conditions can cause a vasovagal reaction, which results in excessive yawning. During a vasovagal reaction, there is increased activity in the vagus nerve. This nerve runs from the brain down to the throat and into the abdomen. When the vagus nerve becomes more active, heart rate and blood pressure drop significantly. The reaction can indicate anything from a sleep disorder to a serious heart condition.</p>
<p>Talk to your doctor if you have noticed a sudden increase in your yawning, especially if you have been yawning frequently for no apparent reason. Only your doctor can determine whether or not the excessive yawning is occurring as a result of a medical problem.</p>
<ul>
	<li>drowsiness, tiredness, or fatigue</li>
	<li>sleep disorders, such as sleep apnea or narcolepsy</li>
	<li>side effects of medications that are used to treat depression or anxiety, such as selective serotonin reuptake inhibitors (SSRIs)</li>
	<li>bleeding in or around the heart </li>
	<li>body inability to control its temperature </li>
</ul>
<h1><b>Treating Excessive Yawning</b></h1>
<p>If medications are causing excessive yawning, your doctor may recommend a lower dosage. Make sure to discuss this with your doctor before making any changes to your medications. You should never stop taking medications without approval from your doctor.</p>
<p>If excessive yawning is occurring as a result of a sleep disorder, your doctor may recommend sleep-aid medications or techniques for getting more restful sleep. These may include:</p>
<ul>
	<li>using a breathing device </li>
	<li>exercising to reduce stress</li>
	<li>adhering to a regular sleep schedule</li>
</ul>
<p>If excessive yawning is a symptom of a serious medical condition, such as epilepsy or liver failure, then the underlying problem must be treated immediately.</p>
