<h1><b>Muscle Cramp</b></h1>
<p>Muscle cramps are sudden, involuntary contractions that occur in various muscles. These contractions are often painful and can affect different muscle groups. Commonly affected muscles include those in the back of your lower leg, the back of your thigh, and the front of your thigh.</p>
<p>The intense pain of a cramp can awaken you at night or make it difficult to walk. A sudden, sharp pain, lasting from a few seconds to 15 minutes, is the most common symptom of a muscle cramp. However, in some cases, a bulging lump of muscle tissue beneath the skin can accompany a cramp as well.</p>
