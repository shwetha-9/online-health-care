<h1><b>What Causes Morning Sickness?</b></h1>
<p>Morning sickness is a common symptom of pregnancy and is marked by nausea and occasional vomiting. Despite the name, morning sickness can cause discomfort at any time of the day.</p>
<P>Morning sickness usually happens within the first four months of pregnancy and is often the first sign that a woman is pregnant.</P>
<p>There is no one cause of morning sickness during pregnancy, and severity varies among women. Increased hormone levels during the first few weeks of pregnancy is among the most common causes. Reduced blood sugar is another common cause of morning sickness.</p>
<p>Other factors can exacerbate morning sickness. These include:</p>
<ul>
	<li>having twins or triplets</li>
	<li>excessive fatigue</li>
	<li>emotional stress</li>
	<li>frequent traveling</li>
</ul>
<p>Morning sickness can vary between pregnancies. While you may have had severe morning sickness during one pregnancy, in future pregnancies it may be very mild. </p>
<h1><b>Prevention</b></h1>
<p>Taking the following steps may help prevent or minimize nausea:</p>
<ul>
	<li>Drink plenty of water.</li>
	<li>Drink water before and after meals.</li>
	<li>Take naps.</li>
	<li>Ventilate your home and workspace to eliminate scents that make you nauseous.</li>
	<li>Avoid spicy foods.</li>
	<li>Eat small meals.</li>
	<li>Avoid fatty foods.</li>
	<li>Take vitamins at night.</li>
	<li>Avoid cigarette smoke.</li>
</ul>
<p>If none of these preventative measures works, or if you experience morning sickness beyond the first three to four months of your pregnancy, itâ€™s important you speak with your doctor. Also, make sure you only try medications and alternative remedies after first discussing these options with your doctor.</p>
