		<li>Calendula cream is an antiseptic used to heal minor wounds and ease skin irritation. Apply the cream directly to the sting site and cover with a bandage.</li>
		<li>Lavender essential oil has anti-inflammatory abilities and can help relieve swelling. Dilute the essential oil with a carrier oil, such a coconut or olive oil. Dab a few drops of the mixture onto the sting site.</li>
