<h1><b>What Causes Itchy Scalp?</b></h1>
<p>An itchy scalp, or scalp pruritus, is a common problem that can cause frustrating symptoms, such as frequent scratching and discomfort. Sometimes, an itchy scalp is accompanied by visible signs, such as scabbed or flaking skin. Other times, your scalp can itch without any skin changes.</p>
<p>Although an itchy scalp doesn’t typically indicate a severe medical concern, it can be a symptom of an underlying condition. Keep reading to discover what might be causing your scalp to itch, as well as treatment and prevention options.</p>
<p>The most common cause of an itchy scalp is seborrheic dermatitis, better known as dandruff. In infants, the condition is called cradle cap or crib cap. This type of dermatitis is most likely to occur in areas of sebaceous or oil-secreting glands, including the scalp and face. If the glands become inflamed, you can experience:</p>
<ul>
<li>itching</li>
<li>flaking</li>
<li>reddened skin</li>
<li>yellow or white scales</li>
</ul>

<h1><b>Prevetion</b></h1>
<p>Reduce your risk for an itchy scalp by washing your hair regularly to remove built-up oils. Wash your hair in warm — but not excessively hot — water to avoid irritating and drying out the scalp. To reduce allergic reactions, try to avoid using products that contain:</p>
<ul>
<li>dyes</li>
<li>fragrances</li>
<li>chemicals</li>
</ul>
<p>Avoid physical contact with people with head lice to prevent spreading lice. This includes refraining from sharing:</p>
<ul>
<li>combs</li>
<li>brushes</li>
<li>towels</li>
<li>hats</li>
</ul>
<h1><b>Home remedies</b></h1>
<p>Treatment for your itchy scalp depends on its cause. For example, dandruff is treated through frequent hair washing with special topical agents. Each scalp medication works in a unique way, such as reducing oil on the scalp or killing fungus.</p>
<p>Some medications that might be used to treat seborrheic dermatitis (dandruff) are:</p>
<ul>
<li>antifungal creams</li>	
<li>coal tar</li>
<li>keratolytics, such as salicylic acid or coal tar</li>
<li>pyrithione zinc</li>
<li>topical steroids</li>
</ul>
<p>Head lice requires medical treatments, such as washing the hair with a pediculicide or using a medicine that kills lice. A fine-tooth comb can remove lice eggs (nits) while the medication kills active lice. In addition to these treatments, people living in close contact may need preventive treatment. All clothes, bedding, and towels that came in contact with the infected person must be washed or dry-cleaned in temperatures greater than 130°F.</p>
<p>If your itchy scalp is due to an allergic reaction, you should refrain from using the product that caused the reaction and speak to a doctor if the reaction is severe.</p>
<p>There are multiple other causes of itchy scalp not covered here. The best way to find out what’s causing your itchy scalp is to have a medical professional take a look at your scalp.</p>
