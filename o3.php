<h1><b>Otorrhea</b></h1>
<p>Ear discharge, also known as otorrhea, is any fluid that comes from the ear.Most of the time, your ears discharge earwax. This is an oil that your body naturally produces. The job of earwax is to make sure that dust, bacteria, and other foreign bodies don’t get into your ear.</p>
<p>However, other conditions, such as a ruptured eardrum, can cause blood or other fluids to drain from your ear. This kind of discharge is a sign that your ear has been injured or infected and requires medical attention.</p>
<ul>
	<li>Keep foreign objects out of your ears to avoid rupturing your eardrum. If you know you’ll be in an area with excessive noise, bring ear plugs or muffs to protect your eardrums.</li>
	<li>breastfeeding may provide infants with protection from ear infections, since they receive their mother’s antibodies in their milk.</li>
	<li>You can prevent swimmer’s ear by making sure to dry your ears after being in the water.</li>
	<li>Pain medications might be needed to deal with any pain or discomfort.</li>
	<li>Most cases of ear trauma also heal without treatment.</li>
	<li>If you have an injury to the ear that causes discharge, that’s another good reason to consult a doctor.</li>
</ul>
<h1><b>What causes ear discharge?</b></h1>
<p>Middle ear infection (otitis media) is a common cause of discharge from the ear. Otitis media occurs when bacteria or viruses make their way into the middle ear. The middle ear is behind the eardrum. It contains three bones called ossicles. These are vital to hearing.Ear infections in the middle ear can cause fluid to build up behind the eardrum. If there is too much fluid, there is a risk of perforation of the eardrum, which can lead to ear discharge.</p>
