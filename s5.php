<p>Some medical conditions can also cause photosensitivity. These include:</p>
<h3>Polymorphous light eruption:</h3><p>People with this condition can develop an itchy rash when they are exposed to the sun. As sun exposure continues and UV tolerance increases, symptoms generally appear less often. Women are two to three times more likely than men to develop this condition.</p>
<h3>Lupus erythematous</h3>
<p>Lupus is a connective tissue disease. Red patches, lumps, and purple spots can develop on areas of your skin exposed to the sun.</p>
<h1><b>Prevention</b></h1>
<p>The best way to prevent symptoms of photosensitivity is to limit the amount of time you spend in the sun. People who are photosensitive should always use sunscreen when outside.</p>
<p>Covering and protecting your skin may also help prevent a reaction. People who are photosensitive can reduce symptoms by wearing hats, sunglasses, and shirts with long sleeves when outside.</p>
<p>These simple tips can help protect your skin and help you live a healthy life.</p>
<h1><b> How is photosensitivity treated?</b></h1>
