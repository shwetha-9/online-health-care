<h1><b> What is unconsciousness?</b></h1>
<p>Unconsciousness is when a person suddenly becomes unable to respond to stimuli and appears to be asleep. A person may be unconscious for a few seconds — as in fainting — or for longer periods of time.</p>
<p>People who become unconscious don’t respond to loud sounds or shaking. They may even stop breathing or their pulse may become faint. This calls for immediate emergency attention. The sooner the person receives emergency first aid, the better their outlook will be.</p>
<p>Unconsciousness can be brought on by a major illness or injury, or complications from drug use or alcohol misuse.</p>
<p>Common causes of unconsciousness include:</p>
<ul>
<li>a car accident</li>	
<li>severe blood loss</li>
<li>a blow to the chest or head</li>
<li>a drug overdose</li>
<li>alcohol poisoning</li>
</ul>
