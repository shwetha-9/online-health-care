<h1><b>Prevention</b></h1>
<h1>Administering First Aid</h1>
<p> If a person is unconscious and you do not know why, immediately check each of the following</p>
<ul>
<li> Is he breathing well? If not, tilt his head way back and pull the jaw and tongue
forward. If something is stuck in his throat, pull it out. If he is not breathing, use
mouth-to-mouth breathing at once </li>
<li> Is he losing a lot of blood? If so, control the bleeding.</li>	
<li>Is he in shock (moist, pale skin; weak, rapid pulse)? If so, lay him with his head
lower than his feet and loosen his clothing.</li>
<li>Could it be heat stroke (no sweat, high fever, hot, red skin)? If so, shade him
from the sun, keep his head higher than his feet, and soak him with cold water (ice
water if possible) and fan him</li>
</ul>
<p>Check again to see if the person is breathing, coughing, or moving. These are signs of positive circulation. If these signs are absent, perform cardiopulmonary resuscitation (CPR) until emergency personnel arrive.</p>
<h1><b>CPR Instructions</b></h1>
<p> Cardiopulmonary resuscitation (CPR) is a way to treat someone when they stop breathing or their heart stops beating.</p>
<p> Before beginning CPR, ask loudly, “Are you okay?” If the person doesn’t respond, begin CPR.</p>
<p> Lay the person on their back on a firm surface.</p>
<p> Kneel next to the person’s neck and shoulders.</p>
<p> Place the heel of your hand over the center of the person’s chest. Put your other hand directly over the first one and interlace your fingers. Make sure that your elbows are straight and move your shoulders up above your hands.</p>
<p> Using your upper body weight, push straight down on the person’s chest at least 1.5 inches for children, or 2 inches for adults. Then release the pressure. Repeat this procedure again up to 100 times per minute. These are called chest compressions.</p>
<h1><b> How is unconsciousness treated?</b></h1>
<p>If unconsciousness is due to low blood pressure, a doctor will administer medication by injection to increase blood pressure. If low blood sugar level is the cause, the unconscious person may need something sweet to eat or a glucose injection.</p>
<p>Medical staff should treat any injuries that caused the person to become unconscious.</p>
