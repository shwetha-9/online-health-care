<h1><b>How can you prevent eye pain?</b></h1>
<p>Prevent many causes of eye pain, such as scratches and burns, by wearing goggles or safety glasses when playing sports, exercising, mowing the lawn, or working with hand tools. Construction workers, welders, and people who work around flying objects, chemicals, or welding gear should always wear protective eye gear.Direct chemicals and potent agents such as household cleaners, detergents, and pest control. Spray away from your body when using them.
Avoid giving your child a toy that can injure their eyes. Toys with spring-loaded components, toys that shoot, and toy swords, guns, and bouncing balls can all injure a child’s eye.</p>
<h1><b>Home Remedies</b></h1>
<ul>
	<li><b>Avoid places with a lot of air movement:</b>This means limiting your exposure to fans and hair dryers, and by wearing wraparound sunglasses when outside on windy days to protect your eyes from drying out.</li>
	<li><b>Turn on a humidifier in the wintertime:</b>Home heating systems can cause the air in your home to dry out, and dry out your eyes. But using a humidifier can help the air stay moist. If you don’t have a humidifier, you can put a pan of water on your radiator to add water into the air.</li>
	<li><b>Rest your eyes:</b>Frequent reading, TV watching, and computer use can dry out your eyes, so it’s important to take breaks so your eyes can regain some of their moisture.</li>
	<li><b>Use warm compresses then wash your eyelids:</b>Placing a warm compress on your eyes then washing your eyelids with baby shampoo helps to release some of the oil in your eyelids’ glands, this improves the quality of your tears. Be sure you completely rinse soap from your eyes when finished to avoid irritating them.</li>
</ul>
