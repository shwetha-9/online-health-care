<h1><b>Ocular Pain</b></h1>
<p>Eye pain is also known as ophthalmalgia. Depending on where you experience the discomfort, eye pain can fall into one of two categories. Ocular pain occurs on the eye’s surface, and orbital pain occurs within the eye. Eye pain is common, but it’s rarely a symptom of a serious condition. Most often, the pain resolves without medicine or treatment.</p>
<p>Eye pain that occurs on the surface may be a scratching, burning, or itching sensation. Surface pain is usually caused by irritation from a foreign object, infection, or trauma. Often, this type of eye pain is easily treated with eye drops or rest.Eye pain that occurs deeper within the eye may feel aching, gritty, stabbing, or throbbing. This kind of eye pain may require more in-depth treatment.</p>
