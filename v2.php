<h1><b>What Causes Vaginal Discharge?</b></h1>
<p>Vaginal discharge is most often a normal and regular occurrence. However, there are certain types of discharge that can indicate an infection. Abnormal discharge may be yellow or green, chunky in consistency, or foul smelling. </p>
<p>Yeast or a bacterial infection usually causes abnormal discharge. If you notice any discharge that looks unusual or smells foul, see your doctor for diagnosis and treatment.</p>
<p>Normal vaginal discharge is a healthy bodily function. It’s your body’s way of cleaning and protecting the vagina. For instance, it’s normal for discharge to increase with sexual arousal and ovulation. Exercise, use of birth control pills, and emotional stress may also result in discharge. </p>
<p>Abnormal vaginal discharge, however, is usually caused by an infection.</p>
