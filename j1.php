<h1><b>Jaundice</b></h1>
<p>“Jaundice” is the medical term that describes yellowing of the skin and eyes. Jaundice itself is not a disease, but it is a symptom of several possible underlying illnesses. Jaundice forms when there is too much bilirubin in your system. Bilirubin is a yellow pigment that is created by the breakdown of dead red blood cells in the liver. </p>
<p>Normally, the liver gets rid of bilirubin along with old red blood cells.Jaundice may indicate a serious problem with the function of your red blood cells, liver, gallbladder, or pancreas.</p>
