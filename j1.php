<h1><b>What’s newborn jaundice?</b></h1>
<p>Newborn jaundice is a yellowing of a baby’s skin and eyes. Newborn jaundice is very common and can occur when babies have a high level of bilirubin, a yellow pigment produced during normal breakdown of red blood cells.</p>
