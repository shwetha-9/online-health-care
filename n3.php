<h1><b>Numbness</b></h1>
<p>Numbness and tingling are unusual prickling sensations that can happen in any part of your body. People generally notice these sensations in hands, feet, arms, and legs. Many things can cause numbness and tingling, including sitting with your legs crossed or falling asleep on your arm.</p>
<p>If numbness and tingling persist and there’s no obvious cause for the sensations, it could be a symptom of a disease or injury, such as multiple sclerosis or carpal tunnel syndrome. Treatment will depend on your diagnosis.The medical term for numbness and tingling is paresthesia.</p>
