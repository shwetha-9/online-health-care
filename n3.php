<h1><b>What Causes Numbness and Tingling?</b></h1>
<p>Many things can cause numbness and tingling, including some medications. Things that we do every day can sometimes cause numbness, including sitting or standing in one position for a long time, sitting with your legs crossed, or falling asleep on your arm. These are all examples of blood flow being cut off to an area for a period of time.There are numerous conditions that can cause you to feel numbness and tingling. For example, an insect or animal bite, toxins found in seafood, a migraine headache, or radiation therapy can give you a feeling of pins and needles. An abnormal level of vitamin B-12, potassium, calcium or sodium is another potential cause. Radiation therapy can also produce this odd sensation.</p>
