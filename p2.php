<h1><b>Painfull Menstruation</b></h1>
<p>Menstruation occurs when the uterus sheds its lining once a month. The lining passes through a small opening in the cervix and out through the vaginal canal.Some pain, cramping, and discomfort during menstrual periods is normal. Excessive pain that causes you to miss work or school is not.</p>
<p>Painful menstruation is also called dysmenorrhea. There are two types of dysmenorrhea: primary and secondary. Primary dysmenorrhea occurs in women who experience pain before and during menstruation. Women who have had normal periods that become painful later in life may have secondary dysmenorrhea. A condition affecting the uterus or other pelvic organs, such as endometriosis or uterine fibroids, can cause this.</p>
