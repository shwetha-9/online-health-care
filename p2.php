<h1><b>Diagnosis</b></h1>
<p>Your doctor will first take your medical history to check for the underlying causes of your painful menstrual periods. They will also perform a physical examination. This will include a pelvic exam to check for any abnormalities in your reproductive system and to look for signs of infection.</p>
