<h1><b>What Causes Burping?</b></h1>
<p>Belching occurs when the stomach fills with swallowed air. There are a number of reasons why more air than normal may be swallowed. The most common reasons are:</p>
<ul>
	<li>eating or drinking too quickly</li>
	<li>drinking carbonated drinks</li>
	<li>anxiety</li>
</ul>
<p>Babies and young children may swallow large amounts of air without realizing it. Babies are burped shortly after drinking breast milk or formula to expel the excess air that was swallowed during feeding.<br>It’s possible to belch when the stomach is not full of air. This is usually because belching has become a habit or a tool for reducing abdominal discomfort. Belching will only relieve discomfort associated with swallowing air. But it’s not uncommon for people to try to relieve other abdominal discomforts in the same way.</p>
<h1><b>How can belching be prevented?</b></h1>
<p>Belching is a natural action. You can control it by avoiding items that are likely to make you belch. If you want to prevent belching, you should:</p>
<ul>
	<li>Sit down and eat each meal slowly.</li>
	<li>Avoid chewing gum or sucking on hard candies.</li>
	<li>Avoid carbonated beverages and alcohol.</li>
	<li>Stop consuming any foods or drinks that make belching more frequent.</li>
	<li>Take probiotic supplements to aid in digestion.</li>
	<li>Avoid anxiety-inducing situations that may cause hyperventilation.</li>
</ul>
<h1><b>How is belching treated?</b></h1>
<p>Normal belching does not require any treatment. However, if belching becomes excessive, you should contact a medical professional to explore possible conditions that may be causing the problem. Treatment will depend on the cause.</p>
<ul>
	<li><b>Self-treatment</b><br>If you’re belching excessively or if your stomach is distended and you cannot expel the air, lying on your side usually helps. Adopting a knees-to-chest position can also be helpful. Hold the position until the gas passes.<br>If you often experience belching, you should avoid:</li>
	<ul>
		<li>eating and drinking quickly</li>
		<li>drinking carbonated beverages</li>
		<li>chewing gum</li>
	</ul>
	<li><b>Medical care</b><br>If your belching has become excessive, it’s important to speak with your doctor. Your doctor will gather information on your symptoms by asking questions about when the excessive belching began and if it has happened before. They will also ask about patterns, such as whether the belching occurs due to nervousness or after consuming a particular food or drink. They may also ask you to keep a food diary for a few days.<br>Make sure you mention any other symptoms you have, even if you don’t think that they’re relevant. This will help your doctor build a full picture of the problem, which will help him or her find the most likely solution.<br>Your doctor may examine you physically and could order further tests such as abdominal X-rays or gastric emptying studies. Other tests include:</li>
	<ul>
		<li>MRI scans</li>
		<li>CT scans</li>
		<li>ultrasound scans</li>
		<li>maldigestion tests</li>
		<li>hydrogen and methane tests</li>
	</ul>
</ul>
<p>These will give your doctor a clear view of your digestive system, which will help them make a diagnosis.</p>
