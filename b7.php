<h1><b>Causes of bowlegs</b></h1>
<ul>
	<li><b>Blount’s disease:</b>In Blount’s disease, which is also called “tibia vara,” a child’s shin develops abnormally, curving below the knees. As your child starts to walk, the bowing of the legs becomes worse. This condition may be apparent early on but in some cases symptoms may not be noticeable until they reach adolescence. Over time, bowlegs can lead to joint problems in their knees.</li>
	<li><b>Rickets:</b>Rickets is a condition resulting from prolonged vitamin D deficiency. This softens and weakens the bones, causing the legs to bow.</li>
	<li><b>Paget’s disease:</b>This metabolic disease negatively affects the way your bones break down and rebuild. As a result, they don’t rebuild as strongly as they should. Over time, this can lead to bowlegs and other joint problems. Paget’s disease is more common in older people and can be successfully managed with early diagnosis and treatment.</li>
	<li><b>Dwarfism:</b>The most common form of dwarfism is caused by a condition known as achondroplasia. This is a bone growth disorder that can result in bowlegs over time.</li>
	<li><b>Other causes</b><br>Bowlegs can also be a result of:</li>
		<ul>
			<li>bone fractures that have not healed properly</li>
			<li>abnormally developed bones, or bone dysplasia</li>
			<li>lead poisoning</li>
			<li>fluoride poisoning</li>
		</ul>
</ul>
<h1><b>Can bowlegs be prevented?</b></h1>
<p>There is no known prevention for bowlegs. In some cases, you may be able to prevent certain conditions that cause bowlegs. For example, you can prevent rickets by making sure your child receives sufficient vitamin D, through both diet and exposure to sunshine. Be sure to talk to your pediatrician if your child still has bowlegs after the age of 2.<br>Early diagnosis and detection of bowlegs will help you and your child manage this condition.<br>Arthritis is the primary long-term effect of bowlegs, and it can be extremely disabling. When it’s severe, it can affect the knees, feet, ankles, and hip joints because of the abnormal stresses applied. People who experience this are typically obese, which compounds the problem.<br>If someone is forced to do a total knee replacement at a young age, then it’s anticipated that a revision would have to be done when the patient is older. Doing a total knee arthroplasty in these people may be difficult because of the surgeries they have already undergone and because of the abnormal alignment of the bones.</p>
<h1><b>Treatment of bowlegs</b></h1>
<p>Treatment is usually not recommended for infants and toddlers unless an underlying condition has been identified. Treatment may be recommended if your case of bowlegs is extreme or getting worse, or if an accompanying condition is diagnosed. Treatment options include:</p>
<ul>
	<li>special shoes</li>
	<li>braces</li>
	<li>casts</li>
	<li>surgery to correct bone abnormalities</li>
	<li>treatment of diseases or conditions that cause bowlegs</li>
</ul>
