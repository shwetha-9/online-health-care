<p>Rhinitis is an inflammation of your nasal cavity lining. It can be allergic or nonallergic. It can also be infectious.Allergic rhinitis can occur when you breathe in an allergen (something that you’re allergic to). It can be seasonal, affecting you at certain times of year, or perennial, affecting you throughout the year.</p>
<p>Allergic rhinitis affects 40 to 60 million Americans, reports the American College of Allergy, Asthma, and Immunology.Nonallergic rhinitis isn’t triggered by a specific allergen. It can affect you for short or long periods of time.</p>
