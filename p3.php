<h1><b>Pale Stools</b></h1>
<p>Normal stools can vary in shades of brown, mostly due to your diet. Pale or clay-colored stools are not normal. If your stools are pale or clay-colored, you may have a problem with the drainage of your biliary system, which is comprised of your gallbladder, liver, and pancreas.Bile salts are released into your stools by your liver, giving the stools a brown color. If your liver is not producing enough bile, or if the flow of the bile is blocked and not draining from your liver, your stools may become pale or clay-colored.</p>
