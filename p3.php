<h1><b>Complications of pale or clay-colored stools?</b></h1>
<p>One of the most common complications of pale or clay-colored stools is jaundice. This is due to a buildup of bile in your body. Jaundice is a yellowing of your skin or around the whites of your eyes. See your doctor immediately if you have signs of jaundice because it may also be a symptom of liver disease.</p>
