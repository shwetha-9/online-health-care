<h1><b>How neck pain is treated?</b></h1>
<p>You doctor will perform a physical exam and take your complete medical history. Be prepared to tell your doctor about the specifics of your symptoms. You should also let them know about all prescription and over-the-counter (OTC) medications and supplements you’ve been taking.Even if it doesn’t seem related, you should also let your doctor know about any recent injuries or accidents you’ve had.Treatment for neck pain depends on the diagnosis.</p>
