<h1><b>Pain in neck</b></h1>
<p>Your neck is made up of vertebrae that extend from the skull to the upper torso. Cervical discs absorb shock between the bones.The bones, ligaments, and muscles of your neck support your head and allow for motion. Any abnormalities, inflammation, or injury can cause neck pain or stiffness.Many people experience neck pain or stiffness occasionally. In many cases, it’s due to poor posture or overuse. Sometimes, neck pain is caused by injury from a fall, contact sports, or whiplash.</p>
<p>Most of the time, neck pain isn’t a serious condition and can be relieved within a few days.But in some cases, neck pain can indicate serious injury or illness and require a doctor’s care.If you have neck pain that continues for more than a week, is severe, or is accompanied by other symptoms, seek medical attention immediately.</p>
